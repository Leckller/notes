          // Operadores Aritméticos //

    // Podemos utilizar os opreadores +, -, * (Multiplicação), / (Divisão), % (Módulo), += (incremento), -= (decremento), ** (expoente)
    
    // O operador "Módulo" é utilizado para pegar o resto da divisão

    let Divisão = 8 / 2
    console.log(Divisão)
    let Modulo = 8 % 2
    console.log(Modulo)
    let Expoente = 2 ** 4
    console.log(Expoente)

    // É possível utilizar os operadores em strings e booleans.
    // Onde na operação booleana o true tem valor 1 e o false valor 0.

    let hello = 'Hello'
    hello = hello + ' ' + 'World'
    console.log(hello)

    let somaBooleana = true + false
    console.log(somaBooleana)